/*
ESTRUTURAS DE REPETIÇÃO:
  for loop: 
    Executar o código uma a certa quantidade de tempo (com contador variável).
    Ex: for (let i = 0; i < 3; i++) 
        { 
        console.log(i);
        }

  for-of loop: 
    Executa para cada elemento em uma matriz.
    Ex: for (const el of array) 
        { 
        console.log(el);
        }
    
  for-in loop: 
    Executa para cada chave em um objeto.
    Ex: for (const key in obj) {
          console.log(key);
          console.log(obj[key]);
        }

  while loop: 
    Executa o código desde que como uma condição é verdadeira.
    Ex: while (isLoggedIn) {
          ...
        }
*/