/*
UNDEFINED, NULL & NaN:
    Undefined (indefinido): Valor padrão de variáveis não inicializadas.            
                            Você não deve atribuir undefined como um valor manualmente.
    Null:  Nunca assumido por inadimplência.
           Você pode atribuir isso é um valor se você quiser "reset" / "clear" uma variável.
    NaN: Tecnicamente, é do tipo número e pode portanto, ser usado em Cálculos. 
         Produz um novo NaN e é o resultado de cálculos inválidos (por exemplo, 3 * 'oi').
*/