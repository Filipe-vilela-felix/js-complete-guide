/*
  - O DOM é uma representação do código HTML de uma página web que foi carregada, e renderizada pelo navegador.

  - O JavaScript pode interagir com o DOM através de vários étodos e funcionalidades que o navegador expõe, como document.querySelector().

  - Esses métodos permitem o JavaScript acessar e manipular elementos HTML da página.

  - O DOM não está restrito aos navegadores. Outras ferramentas e linguagens (como Pyton ou Node.js) também podem analisar e manipular HTML.

  - Além do objeto document, que é o nó raiz do DOM, existe outro objeto global importante: o objeto window.

  - O objeto window representa a janela ou aba ativa do navegador e é o ponto de entrada global para o seu script.

  - O objeto document é na verdade uma propriedade o objeto window.

  - O objeto window fornece acesso a todos os recursos que o navegador deseja expor para você, 
      além de algumas propriedades específicas da janela, como a largura da janela.
*/